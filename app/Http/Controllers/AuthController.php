    public function login(Request $request)
    {
        $credentials = $request->validate([
            'email' => 'required',
            'password' => 'required',

        ]);

        // echo $request->email;

        if (Auth::attempt($credentials)) {
            return redirect('admin');
        } else {
            return redirect()->back()->withErrors(['auth' => 'Login information incorrect, please check CAPSLOCK!']);
        }
    }


